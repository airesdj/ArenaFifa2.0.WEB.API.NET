	IF pInReturno = true THEN
	
		SET _dtInicio = date_add(pDtInicio, INTERVAL pIntervalDias DAY);
		SET _count = pQtMaxTimes - 1;
	
	ELSE
	
		SET _dtInicio = pDtInicio;
	
	END IF;
	
	OPEN tabela_cursor;
	
	get_tabela: LOOP
	
		FETCH tabela_cursor INTO _idTimeHome, _idTimeAway, _nuRodada;
		
